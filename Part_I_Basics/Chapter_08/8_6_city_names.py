# 8-6. City Names:

def city_country(city, country):
    """city formating and your country"""
    cico = f"\n{city}, {country}"
    return cico

# call the function
one = city_country("Machala", "Ecuador")
print(one)
two = city_country("Osaka", "Japan")
print(two)
three = city_country("Toronto", "Canada")
print(three)
